//*****************************************************************************
// 静的メンバ変数
//*****************************************************************************
LPDIRECT3DTEXTURE9 CNumber::m_pTexture = nullptr;

//=============================================================================
// コンストラクタ
//=============================================================================
CNumber::CNumber()
{
	//オブジェクトの種類設定
	SetObjType(EObject::OBJ_SCORE);
}

//=============================================================================
// デストラクタ
//=============================================================================
CNumber::~CNumber()
{
}

//=============================================================================
// テクスチャの読み込み
//=============================================================================
HRESULT CNumber::Load()
{
	//デバイスの取得
	LPDIRECT3DDEVICE9 pDevice = CManager::GetRenderer()->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice, "data/TEXTURE/number000.png", &m_pTexture);

	return S_OK;
}

//=============================================================================
// テクスチャの破棄
//=============================================================================
void CNumber::Unload()
{
	if (m_pTexture != nullptr)
	{
		m_pTexture->Release();
		m_pTexture = nullptr;
	}
}

//=============================================================================
// ポリゴンの初期化
//=============================================================================
HRESULT CNumber::Init()
{
	//生成時に使用するサイズの設定
	CObject2D::Init();
	CObject2D::BindTexture(m_pTexture);
	CObject2D::SetAnimation(0, 0, DIVISION_U, DIVISION_V);

	return S_OK;
}

//=============================================================================
// ポリゴンの終了
//=============================================================================
void CNumber::Uninit()
{
	CObject2D::Uninit();
}

