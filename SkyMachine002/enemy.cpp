#include "enemy.h"
#include "manager.h"
#include "renderer.h"
#include "explosion.h"

//-----------------------------------------------------------------------------------------------
// 変数変数
//-----------------------------------------------------------------------------------------------
// 幅
const float CEnemy::MOVE_DEFAULT = 4.0f;
// 高さ
const float CEnemy::SIZE = 50.0f;

//-----------------------------------------------------------------------------------------------
// 静的メンバ変数
//-----------------------------------------------------------------------------------------------
// テクスチャのポインタ
LPDIRECT3DTEXTURE9 CEnemy::m_pTexture = nullptr;

//-----------------------------------------------------------------------------------------------
// コンストラクタ
//-----------------------------------------------------------------------------------------------
CEnemy::CEnemy() :
	m_move(0.0f, 0.0f, 0.0f), m_nLife(0)
{
	SetObjectType(EObject::TYPE_ENEMY);
}

//-----------------------------------------------------------------------------------------------
// デストラクタ
//-----------------------------------------------------------------------------------------------
CEnemy::~CEnemy()
{

}

//-----------------------------------------------------------------------------------------------
// 生成
//-----------------------------------------------------------------------------------------------
CEnemy* CEnemy::Create(const D3DXVECTOR3& pos)
{
	// ポインタクラスを宣言
	CEnemy* pEnemy = new CEnemy;

	if (pEnemy != nullptr)
	{// もしnullptrではなかったら
		// 初期化
		pEnemy->Init(pos);

		// テクスチャの設定
		pEnemy->BindTexture(m_pTexture);
	}

	return pEnemy;
}

//-----------------------------------------------------------------------------------------------
// テクスチャの読み込み
//-----------------------------------------------------------------------------------------------
HRESULT CEnemy::Load()
{
	// デバイスの取得
	LPDIRECT3DDEVICE9 pDevice = CManager::GetRenderer()->GetDevice();

	// テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice,
		"data/TEXTURE/enemy000.png",
		&m_pTexture);

	return S_OK;
}

//-----------------------------------------------------------------------------------------------
// テクスチャの削除
//-----------------------------------------------------------------------------------------------
void CEnemy::Unload()
{
	// テクスチャの破棄
	if (m_pTexture != nullptr)
	{
		m_pTexture->Release();
		m_pTexture = nullptr;
	}
}

//-----------------------------------------------------------------------------------------------
// 初期化
//-----------------------------------------------------------------------------------------------
HRESULT CEnemy::Init(const D3DXVECTOR3& pos)
{
	// 移動量
	m_move.x = MOVE_DEFAULT;
	// 寿命
	m_nLife = LIFE;
	// サイズ
	CObject2D::SetSize(SIZE, SIZE);

	CObject2D::Init(pos);

	return S_OK;
}

//-----------------------------------------------------------------------------------------------
// 終了
//-----------------------------------------------------------------------------------------------
void CEnemy::Uninit()
{
	CObject2D::Uninit();
}

//-----------------------------------------------------------------------------------------------
// 更新
//-----------------------------------------------------------------------------------------------
void CEnemy::Update()
{
	// 位置の取得
	D3DXVECTOR3 pos = CObject2D::GetPosition();
	//向きの取得
	float fRot = CObject2D::GetRot();

	// 移動量の更新
	pos += m_move;
	fRot += 0.1f;

	if (m_nLife == 0)
	{// ライフが0
		// 爆発の生成
		CExplosion::Create(pos);
		// 破棄
		Uninit();
	}
	else
	{
		//向きの更新
		CObject2D::SetRot(fRot);
		// 位置の更新
		CObject2D::SetPosition(pos);
	}
}

//-----------------------------------------------------------------------------------------------
// 描画
//-----------------------------------------------------------------------------------------------
void CEnemy::Draw()
{
	CObject2D::Draw();
}